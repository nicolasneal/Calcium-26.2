    private static final List<Object> EMPTY = List.of();

    private record VariantGroup(Item item, Predicate<ItemStack> filter) {}

    private static VariantGroup variantsOf(Item item) {
        return new VariantGroup(item, stack -> true);
    }

    private record FixedStack(Supplier<ItemStack> factory) {}

    private static FixedStack potionOf() {
        return new FixedStack(() -> PotionContents.createItemStack(Items.POTION, Potions.WATER));
    }

    public static void initialize() {

        for (ResourceKey<CreativeModeTab> tab : TAB_ORDER) {

            List<Object> entries = TABS.getOrDefault(tab, EMPTY);
            Event<CreativeModeTabEvents.ModifyOutput> event = CreativeModeTabEvents.modifyOutputEvent(tab);
            event.addPhaseOrdering(Event.DEFAULT_PHASE, FINAL_PHASE);
            event.register(FINAL_PHASE, output -> {
                apply(output.getDisplayStacks(), entries);
                apply(output.getSearchTabStacks(), entries);
            });

        }

    }

    private static void apply(List<ItemStack> stacks, List<Object> entries) {
        List<ItemStack> original = List.copyOf(stacks);
        List<ItemStack> result = new ArrayList<>();
        for (Object entry : entries) {
            if (entry instanceof VariantGroup(Item item, Predicate<ItemStack> filter)) {
                for (ItemStack stack : original) {
                    if (stack.is(item) && filter.test(stack)) result.add(stack.copy());
                }
            } else if (entry instanceof FixedStack(Supplier<ItemStack> factory)) {
                result.add(factory.get());
            } else {
                result.add(new ItemStack(((ItemLike) entry).asItem()));
            }
        }
        stacks.clear();
        stacks.addAll(result);
    }

    private static List<Object> items(Object... parts) {
        List<Object> result = new ArrayList<>();
        for (Object part : parts) {
            if (part instanceof List<?> group) {
                result.addAll(group);
            } else {
                result.add(part);
            }
        }
        return result;
    }

    private static List<ItemLike> colorsOf(ColorCollection<Item> collection) {
        List<ItemLike> result = new ArrayList<>();
        for (DyeColor color : DISPLAY_COLOR_ORDER) result.add(collection.pick(color));
        return result;
    }

    private static List<ItemLike> copperStatesOf(WeatheringCopperCollection.ByState<Item> byState) {
        List<ItemLike> result = new ArrayList<>();
        for (WeatheringCopper.WeatherState state : WeatheringCopper.WeatherState.values()) result.add(byState.pick(state));
        return result;
    }

